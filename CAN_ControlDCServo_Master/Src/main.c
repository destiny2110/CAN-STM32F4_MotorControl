/**
  * @brief  Main program
  * @param  None
  * @retval None
  */
int main(void)
{
  /* STM32F4xx HAL library initialization:
       - Configure the Flash prefetch, instruction and Data caches
       - Configure the Systick to generate an interrupt each 1 msec
       - Set NVIC Group Priority to 4
       - Global MSP (MCU Support Package) initialization
     */
  HAL_Init();  

	SystemClock_Config();
	
	//LED init
	LED_Initialize();
  
	//Encoder init
	init_Timer_ENCODER();
  
	//Uart init
	v_UART_service_init(115200);
	
	// CAN init
	v_CAN_service_init();
	
  /* Infinite loop */
  while(1)
  {
		v_CAN_process();
		v_UART_process();
		
		HAL_Delay(1);
	} 
}

